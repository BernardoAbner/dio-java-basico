package dio.bernardo.operadores;

public class operadores3 {

    public static void main (String [] args){

        int numero = 5;
        System.out.print(numero ++); 
        System.out.print(numero);


        System.out.print( numero --);
        System.out.print(numero);



        boolean variavel = true;

        System.out.print(!variavel);

        System.out.print(variavel);

        variavel = !variavel;

        System.out.print(variavel);
    }

    
}
